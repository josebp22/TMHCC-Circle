import math
pi=math.pi

radius=int(input("What is your circle radius: "))

circumference=pi*radius*2
area_of_circle=pi*radius*radius



print(f"Area of Circle= {round(area_of_circle,2)} \U0001F61B" + 3*"\U0001F61B")
print(f"Circumference= {round(circumference,2)} \U0001F61B" + 3*"\U0001F61B")

